=begin
PROBLEM
-input: takes 0 or more agruments (integers) and block
-output: total number of arguments for which the block returns true
  - if no argument is passed, return 0

EXAMPLES
p count(1, 3, 6) { |value| value.odd? } == 2
p count(1, 3, 6) { |value| value.even? } == 1
p count(1, 3, 6) { |value| value > 6 } == 0
p count(1, 3, 6) { |value| true } == 3
p count() { |value| true } == 0
p count(1, 3, 6) { |value| value - 6 } == 3

ALGORITHM
1. for each argument passed, iterate through and count the 
    number of time the block returns true for each element
=end

def count(*arguments)
  total = 0
  arguments.each { |element| total += 1 if yield(element) }
  total
end

p count(1, 3, 6) { |value| value.odd? } == 2
p count(1, 3, 6) { |value| value.even? } == 1
p count(1, 3, 6) { |value| value > 6 } == 0
p count(1, 3, 6) { |value| true } == 3
p count() { |value| true } == 0
p count(1, 3, 6) { |value| value - 6 } == 3